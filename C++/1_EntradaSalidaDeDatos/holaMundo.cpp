/* Programa para imprimir texto en pantalla */
#include <iostream> 
// '#include' directiva del preprocesador
// 'iostream' libreria estandar Entrada/Salida

// funcion principal 'main' que retorna un tipo de dato entero 
// ejecucion del programa
int main() {
  // imprime un texto en pantalla
  std::cout << "Hola Mundo!!\n"; // '\n' salto de linea, es como dar enter

  // indica que el programa se ejecuto correctamente
  return 0;
}

